// ── Fallback while Monaco loads ───────────────────────────────────────────────

function EditorSkeleton({ height }) {
  return (
    <div
      className="ce-skeleton"
      style={{ height }}
      aria-label="Loading code editor…"
      aria-busy="true"
    >
      {/* Fake gutter + line shimmer rows */}
      {Array.from({ length: 8 }, (_, i) => (
        <div key={i} className="ce-skeleton-row" style={{ animationDelay: `${i * 60}ms` }}>
          <span className="ce-skeleton-ln" />
          <span className="ce-skeleton-code" style={{ width: `${45 + (i * 17) % 42}%` }} />
        </div>
      ))}
    </div>
  );
}

// ── Main component ────────────────────────────────────────────────────────────

export default function CodeEditor({
  value = STARTER_RUST_SOURCE,
  onChange,
  language = 'rust',
  theme = 'magnetite-dark',
  height = '420px',
  readOnly = false,
  filename,
  className = '',
}) {
  const editorRef = useRef(null);

  const handleMount = useCallback((editor, monaco) => {
    editorRef.current = editor;
    // Define and apply our custom theme
    defineTheme(monaco);
    monaco.editor.setTheme(theme);
    // Give focus for immediate keyboard use
    editor.focus();
  }, [theme]);

  const handleChange = useCallback((val) => {
    if (onChange) onChange(val ?? '');
  }, [onChange]);

  return (
    <div className={`ce-root ${className}`.trim()}>
      {/* Header bar */}
      <div className="ce-header" aria-hidden="true">
        <div className="ce-dots">
          <span className="ce-dot ce-dot--red" />
          <span className="ce-dot ce-dot--amber" />
          <span className="ce-dot ce-dot--green" />
        </div>
        {filename && (
          <span className="ce-filename">{filename}</span>
        )}
        <div className="ce-lang-badge">{language}</div>
      </div>

      {/* Editor viewport */}
      <div className="ce-viewport">
        <Suspense fallback={<EditorSkeleton height={height} />}>
          <MonacoEditor
            height={height}
            language={language}
            value={value}
            theme={theme}
            options={buildEditorOptions(readOnly)}
            onMount={handleMount}
            onChange={handleChange}
            loading={<EditorSkeleton height={height} />}
          />
        </Suspense>
      </div>
    </div>
  );
}
