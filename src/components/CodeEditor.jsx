/**
 * CodeEditor — Monaco-backed in-browser code editor.
 *
 * Monaco is loaded lazily via dynamic import so it only appears in a separate
 * chunk and does not inflate the main bundle.  The `@monaco-editor/react`
 * package handles this automatically via its own loader.
 *
 * Props:
 *   value        string   — current source text
 *   onChange     fn       — called with new string on every change
 *   language     string   — Monaco language id (default: 'rust')
 *   theme        string   — Monaco theme (default: 'magnetite-dark')
 *   height       string   — CSS height (default: '420px')
 *   readOnly     bool     — disable editing (default: false)
 *   filename     string   — shown in the header bar (optional)
 *   className    string   — extra class on the wrapper (optional)
 */

import { lazy, Suspense, useCallback, useRef } from 'react';
import './CodeEditor.css';

// Lazy-load Monaco — this will be split into a separate chunk by Vite/Rollup.
const MonacoEditor = lazy(() => import('@monaco-editor/react'));

// ── Starter Rust source shown when no value is provided ──────────────────────

export const STARTER_RUST_SOURCE = `//! magnetite-game — starter game source
//! Generated by Magnetite Game Studio.
//!
//! Implement the \`AuthoritativeGame\` trait methods below.
//! Run locally with:  magnetite dev

use magnetite_runtime::prelude::*;
use serde::{Deserialize, Serialize};

/// Per-player state stored server-side.
#[derive(Clone, Debug, Default, Serialize, Deserialize)]
pub struct PlayerState {
    pub position: Vec2,
    pub health: u8,
    pub score: u32,
}

/// Shared world state broadcast to all clients each tick.
#[derive(Clone, Debug, Default, Serialize, Deserialize)]
pub struct WorldState {
    pub tick: u64,
    pub players: std::collections::HashMap<PlayerId, PlayerState>,
}

/// Client -> Server messages.
#[derive(Clone, Debug, Deserialize)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum ClientMsg {
    Move { dx: f32, dy: f32 },
    Action { kind: String },
}

/// Server -> Client messages.
#[derive(Clone, Debug, Serialize)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum ServerMsg {
    StateSnapshot(WorldState),
    PlayerJoined { id: PlayerId },
    PlayerLeft   { id: PlayerId },
}

pub struct Game {
    world: WorldState,
}

#[async_trait::async_trait]
impl AuthoritativeGame for Game {
    type PlayerState = PlayerState;
    type WorldState  = WorldState;
    type ClientMsg   = ClientMsg;
    type ServerMsg   = ServerMsg;

    fn new() -> Self {
        Self { world: WorldState::default() }
    }

    async fn on_player_join(&mut self, id: PlayerId) -> Vec<(PlayerId, ServerMsg)> {
        self.world.players.insert(id, PlayerState::default());
        broadcast_all(&self.world, ServerMsg::PlayerJoined { id })
    }

    async fn on_player_leave(&mut self, id: PlayerId) -> Vec<(PlayerId, ServerMsg)> {
        self.world.players.remove(&id);
        broadcast_all(&self.world, ServerMsg::PlayerLeft { id })
    }

    async fn on_message(
        &mut self,
        from: PlayerId,
        msg: ClientMsg,
    ) -> Vec<(PlayerId, ServerMsg)> {
        match msg {
            ClientMsg::Move { dx, dy } => {
                if let Some(p) = self.world.players.get_mut(&from) {
                    p.position.x += dx;
                    p.position.y += dy;
                }
            }
            ClientMsg::Action { kind } => {
                log::debug!("action: {kind} from {from:?}");
            }
        }
        vec![]
    }

    async fn tick(&mut self) -> Vec<(PlayerId, ServerMsg)> {
        self.world.tick += 1;
        broadcast_all(&self.world, ServerMsg::StateSnapshot(self.world.clone()))
    }
}

/// Helper: broadcast a message to every connected player.
fn broadcast_all(world: &WorldState, msg: ServerMsg) -> Vec<(PlayerId, ServerMsg)> {
    world.players.keys().map(|id| (*id, msg.clone())).collect()
}
`;

// ── Monaco editor options ────────────────────────────────────────────────────

function buildEditorOptions(readOnly) {
  return {
    readOnly,
    minimap: { enabled: false },
    scrollBeyondLastLine: false,
    fontSize: 13,
    fontFamily: "'JetBrains Mono', ui-monospace, 'Cascadia Code', 'Fira Code', Consolas, monospace",
    fontLigatures: true,
    lineHeight: 22,
    padding: { top: 16, bottom: 16 },
    renderLineHighlight: 'gutter',
    smoothScrolling: true,
    cursorBlinking: 'smooth',
    cursorSmoothCaretAnimation: 'on',
    bracketPairColorization: { enabled: true },
    formatOnPaste: true,
    tabSize: 4,
    wordWrap: 'off',
    scrollbar: {
      verticalScrollbarSize: 6,
      horizontalScrollbarSize: 6,
      useShadows: false,
    },
    overviewRulerLanes: 0,
    hideCursorInOverviewRuler: true,
    glyphMargin: false,
    lineNumbersMinChars: 3,
    folding: true,
    foldingHighlight: false,
  };
}

